package com.ustglobal.springcore.book;

public class Author {
	private String name;
	private String PenName;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPenName() {
		return PenName;
	}
	public void setPenName(String penName) {
		PenName = penName;
	}
	
	

}
